package ex_Primitve_Data_Type_130724;

public class Lab010 {
    public static void main(String[] args) {

        // assignment operator
        int c = 30; // = is a assignment operator
        System.out.println(c);

        // unary operator - which applies only in 1 operand
        int a = 10; // + unary plus default
        int b = -20; // - unary minus
        System.out.println(a);
        System.out.println(b + "\n");

        // arithmetical operator
        int d = 88;
        int e = 99;
        System.out.println(d+e);
        System.out.println(d-e);
        System.out.println(d*e);
        System.out.println(e/d);
        System.out.println(e%d); // modulas


    }
}
